def main():
    print("16-4=()".format(16 - 4))
    print("20+15=()".format(20 + 15))
    print("4*5=()".format(4 * 5))
    print("12/6=()".format(12 / 6))
    print("4/9=()".format(4 / 9))
    print("4+17*2=()".format(4 + 17 * 2))
    print("7^3=()".format(7 ^ 3))  # bitwise operator
    print("3**6=()".format(3**6))  # exponent
    print("25**0.5=()".format(25**0.5))  # square root


if __name__ == "__main__":
    main()
